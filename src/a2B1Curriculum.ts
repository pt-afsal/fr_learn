function uniqueChoices(values: string[]) {
  const seen = new Set<string>()
  return values.filter((value) => {
    const normalized = value.toLocaleLowerCase('fr-FR')
    if (seen.has(normalized)) return false
    seen.add(normalized)
    return true
  })
}
