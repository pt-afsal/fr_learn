export function completeQuestionSets(topics: SkillTopic[], questions: Question[], targetSize = 10): Question[] {
  const generated: Question[] = []
  const existingIds = new Set(questions.map((question) => question.id))
  const examples = topics.flatMap((topic) =>
    topic.examples.map((example) => ({
      topicId: topic.id,
      example,
      titleEn: topic.titleEn,
      titleFr: topic.titleFr,
    })),
  )

  topics.forEach((topic, topicIndex) => {
    let count =
      questions.filter((question) => question.topicId === topic.id).length +
      generated.filter((question) => question.topicId === topic.id).length

    while (count < targetSize) {
      const ownExample = topic.examples[count % Math.max(1, topic.examples.length)] ?? topic.titleFr
      const distractors = examples
        .filter((item) => item.topicId !== topic.id)
        .slice(topicIndex + count, topicIndex + count + 8)
        .map((item) => item.example)
      const choices = uniqueChoices([ownExample, ...distractors]).slice(0, 4)
      const id = `q-${topic.id}-set-${count + 1}`

      if (!existingIds.has(id)) {
        generated.push({
          id,
          topicId: topic.id,
          type: 'multiple-choice',
          promptEn: [
            `Choose the sentence that best illustrates “${topic.titleEn}”.`,
            `Which example uses the rule “${topic.titleEn}” correctly?`,
            `Select the best model sentence for “${topic.titleEn}”.`,
            `Which sentence should you keep as a correct example of “${topic.titleEn}”?`,
          ][count % 4],
          promptFr: [
            `Choisissez la phrase qui illustre le mieux « ${topic.titleFr} ».`,
            `Quel exemple utilise correctement la règle « ${topic.titleFr} » ?`,
            `Sélectionnez la meilleure phrase modèle pour « ${topic.titleFr} ».`,
            `Quelle phrase faut-il retenir comme exemple correct de « ${topic.titleFr} » ?`,
          ][count % 4],
          choices,
          correctAnswer: ownExample,
          explanationEn: `This example belongs to ${topic.titleEn}. Review the rule and say the example aloud before moving on.`,
          explanationFr: `Cet exemple appartient à ${topic.titleFr}. Révisez la règle et dites l’exemple à voix haute avant de continuer.`,
        })
      }
      count += 1
    }
  })

  return generated
}

